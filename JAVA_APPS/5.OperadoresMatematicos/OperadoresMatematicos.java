public class OperadoresMatematicos {
    
    public static void main(String[] args) {
        
        // OPERADORES MATEMATICOS - SUMATORIA, RESTA, MULTIPLICACION, DIVISION, MODULO

        int num1 = 20;
        int num2 = 15;
        int num3 = 5;


        // SUMA
        int resuladoSuma = num1 + num2; // 20 + 15 = 35

        // RESTA
        int resultadoResta = num1 - num2; // 20 - 15 = 5

        // MULTIPLICACION
        int resultadoMultiplicacion = num1 * num2; // 20 * 15 = 300

        // DIVISION
        int resultadoDivision = num1 / num2; // 20 / 15 = 1

        // MODULO
        int resultadoModulo = num1 % num2; // 20 % 15 = 5

        System.out.println((num1 + num2) * num3); // (20 + 15) * 5 = 175


    }
}
