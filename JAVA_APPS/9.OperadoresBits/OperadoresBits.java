public class OperadoresBits {
    
public static void main(String[] args) {
    // & es un operador bit a bit que realiza una operación AND a nivel de bits entre dos números
    int a = 5; // en binario: 0101
    int b = 3; // en binario: 0011

    System.out.println("a & b = " + (a & b)); // 1 (en binario: 0001)
}
    
}
