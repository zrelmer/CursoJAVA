public class Decremento {
    
    public static void main(String[] args) {
        int a = 5;
        int b = 5;

        System.out.println(a--); // Imprime 5 (Disminuye a 4 después de imprimir)
        System.out.println(a);   // Imprime 4 (Valor actual de a)
        // System.out.println(a--);
        // System.out.println(a);
        System.out.println(--b); // Imprime 4 (Disminuye a 4 antes de imprimir)
    }
}
